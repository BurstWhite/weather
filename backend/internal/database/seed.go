type seedCity struct {
	LocationID string
	Name       string
	Adm1       string
	Adm2       string
	Lat        float64
	Lon        float64
}

func seed() error {
	count := 0
	err := DB.QueryRow("SELECT COUNT(*) FROM cities").Scan(&count)
	if err != nil {
		return err
	}
	if count > 0 {
		return nil
	}

	tx, err := DB.Begin()
	if err != nil {
		return err
	}
	defer tx.Rollback()

	stmt, err := tx.Prepare("INSERT OR IGNORE INTO cities(location_id, name, adm1, adm2, lat, lon) VALUES(?, ?, ?, ?, ?, ?)")
	if err != nil {
		return err
	}
	defer stmt.Close()

	for _, c := range chinaCities() {
		_, err := stmt.Exec(c.LocationID, c.Name, c.Adm1, c.Adm2, c.Lat, c.Lon)
		if err != nil {
			return fmt.Errorf("seed %s: %w", c.Name, err)
		}
	}
	return tx.Commit()
}

